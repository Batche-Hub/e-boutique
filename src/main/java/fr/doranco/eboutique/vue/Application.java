    /**
     * Méthode relativement simple qui permet de passer au crible une liste de
     * jTextfield qu'on met en paramètre. Cela permet de remplacer de façon
     * élégante les if sans fin. Si un des jTextfields, une fois raccourci des
     * espaces, est vide, la méthode s'arrête et met en rouge le premier champ
     * vide. Si aucun des jTextfields n'est vide, alors on retourne un
     * false à la question "Est-ce qu'un des jTextfield est vide ?".
     *
     * @param jTextFieldsList
     * @return
     */
    private boolean isAJtextFieldIsEmpty(List jTextFieldsList) {

        ArrayList<JTextField> jTextFieldsArrayList = new ArrayList<>();
        jTextFieldsArrayList.addAll(jTextFieldsList);

        for (JTextField jTextField : jTextFieldsArrayList) {
            if (jTextField.getText().trim().isEmpty()) {
                jTextField.setBackground(Color.red);

                JOptionPane.showMessageDialog(jPanelSeConnecter, "Tous les champs doivent être remplis");

                jTextField.setBackground(new java.awt.Color(136, 186, 220));

                return true;
            }
        }
        return false;

    }

